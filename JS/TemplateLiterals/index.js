//Template Literals which are new ES6 Feature which is embed variables inside the strings using the backticks ``
//Easy string formatting
//cleaner code
//Multiple variables
//Multiline string
let name="Teja"
let company="NYBInfotech"
let role="Associate Software Engineer"
//Template literal is the ES6 new feature where is to write the output without any confusion rather than old method 
console.log(`Hi My name is ${name} and I work at ${company} and My role is ${role}`)

//Examples of dynamic string formatting

//Shopping Details
let product="Laptop"
let price=50000
console.log(`The ${product} price is ${price}`)

//Employee Details
let employee="Teja"
let salary=25000
console.log(`${employee} Salary is ${salary}`)

//Marks Details
let marks=85
console.log(`Student scored ${marks} marks`)

let city="Hyderabad"
